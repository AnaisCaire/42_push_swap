#include "push_swap.h"

int	syntax_error(char *strnb)
{
	int i = 0;
	while(strnb[i])
	{
		if (!((strnb[i] == '+' || strnb[i] == '-')) 
			&& (strnb[1] >= '0' && strnb[1] <= '9'))
			return (1);
		if ((strnb[0] == '+' || strnb[0] == '-') 
			&& (strnb[i] >= '0' && strnb[i] <= '9'))
			i++;
	}
}